public class switch_statement {
    public static void main(String[] args){
        int num=2;
        switch(num){
            case 1:System.out.println("Ayush");
            break;
            case 2:System.out.println("Shubham");
            break;
            case 3:System.out.println("Alfred");
            break;
            default:System.out.println("No one");
       }
    }
}
