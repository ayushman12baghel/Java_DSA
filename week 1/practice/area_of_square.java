import java.util.*;

public class area_of_square {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        System.out.print("Enter the side of square: ");
        int a=sc.nextInt();
        int area=a*a;
        System.out.print("The area of Square is: ");
        System.out.println(area);
    }
}
