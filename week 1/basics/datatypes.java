public class datatypes {
    public static void main(String[] args) {
        byte b = 8;// 1 byte = 256 numbers (-128 to 127)
        System.out.println(b);
        char ch = 'a'; // 2 bytes
        System.out.println(ch);
        boolean var = true; // 1 bytes
        double price=120.850;
        int num=25;// 4 bytes
        // 1 bytes = 8 bits;
        // long = 8 bytes
        //double= 8 bytes
        //float = 4 bytes
        short numb=240;// 2 bytes
        System.out.println(var);
        System.out.println(price);
        System.out.println(num);
        System.out.println(numb);
    }
}
